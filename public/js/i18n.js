// Active language — default Italian
let LANG = 'en';
function t(key, ...args) {
  const val = TRANSLATIONS[LANG]?.[key] ?? TRANSLATIONS['it'][key];
  if (typeof val === 'function') return val(...args);
  return val ?? key;
}
function setLang(lang) {
  LANG = lang;
  document.documentElement.lang = lang;
}

// Returns the display name for a resource, checking skin override first
function resName(res) {
  const skinOverride = SKIN?.resourceNames?.[res];
  if (skinOverride) return skinOverride;
  return (t('res') || {})[res] || res;
}

// Returns the emoji for a resource, checking skin override first
function resEmoji(res) {
  const skinOverride = SKIN?.resourceEmojis?.[res];
  if (skinOverride) return skinOverride;
  const defaults = { wood:'🪵', brick:'🧱', sheep:'🐑', wheat:'🌾', ore:'🪨', desert:'🏜️', any:'🌀' };
  return defaults[res] || res;
}
// Returns a UI label for a named game element, checking skin override first.
// key examples: 'road', 'settlement', 'city', 'port', 'robber',
//               'knight', 'monopoly', 'year_of_plenty', 'road_building'
function skinLabel(key, fallback) {
  const skinOverride = SKIN?.labels?.[key];
  if (skinOverride) return skinOverride;
  return fallback;
}

